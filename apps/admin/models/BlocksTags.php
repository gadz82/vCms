    public function columnMap()
    {
        return [
            'id'                 => 'id',
            'descrizione'        => 'descrizione',
            'data_creazione'     => 'data_creazione',
            'data_aggiornamento' => 'data_aggiornamento',
            'id_utente'          => 'id_utente',
            'attivo'             => 'attivo'
        ];
    }

}
